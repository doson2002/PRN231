package com.example.prn231.Model;

public class Certificate {
    public String cetificateName;
    public String cetificateDesciption;
    public String cetificateImageUrl;
    public String createdOnUtc;

    public String getCetificateName() {
        return cetificateName;
    }

    public void setCetificateName(String cetificateName) {
        this.cetificateName = cetificateName;
    }

    public String getCetificateDesciption() {
        return cetificateDesciption;
    }

    public void setCetificateDesciption(String cetificateDesciption) {
        this.cetificateDesciption = cetificateDesciption;
    }

    public String getCetificateImageUrl() {
        return cetificateImageUrl;
    }

    public void setCetificateImageUrl(String cetificateImageUrl) {
        this.cetificateImageUrl = cetificateImageUrl;
    }

    public String getCreatedOnUtc() {
        return createdOnUtc;
    }

    public void setCreatedOnUtc(String createdOnUtc) {
        this.createdOnUtc = createdOnUtc;
    }
}
